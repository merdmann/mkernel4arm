/*
 * This module contains the startup and shutdown procedures of the
 * OS.
 */

#include "kernel.h"
#include "panic.h"
#include "execution.h"
#include "machdep.h"
#include "proc.h"

/* this is generated by the oil compiler */
extern void _oil_device_init(void);

/* current application mode */
static AppModeType active_mode ;

AppModeType GetActiveApplicationMode ( void ) {
	return active_mode;
}

/*
 * This is the main program. It will never return, since after the
 * _START as task will become active.
 */
void StartOS(AppModeType mode) {
    // some hardware init
    active_mode = mode;
    _os_mode = KERNEL_MODE;
   
    _oil_init_application_mode(mode);
    _os_initialize_scheduler();
    _oil_device_init();

    _os_schedule();

    _os_mode = USER_MODE;

    StartupHook();
    /* we are switching now to user mode and start the idle task */
    _machdep_boot();            // setup stack model etc ....
    _os_initialize_alarm();
    _machdep_yield();
}

/*
 * Shutdown of the system in case of an error
 */
void ShutdownOS(StatusType error) {
    _machdep_trace((unsigned)error);
}
